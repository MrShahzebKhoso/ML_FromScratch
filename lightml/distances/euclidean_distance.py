import numpy as np

def euclidean_distance(x, y):
    """
    Compute the Euclidean (L2) distance between vectors x and y.
    Must return a float.
    """
    x = np.asarray(x, float)
    y = np.asarray(y, float)
     # dist = np.sqrt(np.sum((x - y( **2)))
    L1 =  np.sqrt(np.sum((x - y) **2))
    return float(L1)